"""Core helpers for pruning generated/vendored files from the benchmark corpus.

The corpus is cloned from pinned commits by bench/setup_repos.sh. This pass removes
source files that are not useful refactoring targets, while refusing to delete files
referenced by the active gold labels.
"""

from __future__ import annotations

import hashlib
import json
import os
import re
import shutil
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable


PROJECT_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_REPOS_ROOT = PROJECT_ROOT / "bench" / "repos"
DEFAULT_LABELS = PROJECT_ROOT / "bench" / "labels" / "refactoring_families.v5.json"
DEFAULT_MANIFEST = PROJECT_ROOT / "bench" / "labels" / "prune_manifest.json"
DEFAULT_CORPUS = PROJECT_ROOT / "bench" / "goldens" / "corpus.json"
BANNER_BYTES = 8192

SOURCE_SUFFIXES = (
    ".c",
    ".cc",
    ".cjs",
    ".cpp",
    ".cs",
    ".cts",
    ".cxx",
    ".go",
    ".h",
    ".hh",
    ".hpp",
    ".hxx",
    ".java",
    ".js",
    ".jsx",
    ".mjs",
    ".mts",
    ".py",
    ".pyi",
    ".rake",
    ".rb",
    ".rs",
    ".svelte",
    ".swift",
    ".ts",
    ".tsx",
)

SKIP_DIRS = {
    ".git",
    ".hg",
    ".svn",
    "__pycache__",
    ".mypy_cache",
    ".pytest_cache",
    "node_modules",
    "target",
}

BANNER_RULES: tuple[tuple[str, re.Pattern[str]], ...] = (
    ("generated-banner", re.compile(r"@generated\b", re.I)),
    (
        "generated-banner",
        re.compile(r"\bcode generated\b.*\bdo not edit\b", re.I | re.S),
    ),
    (
        "generated-banner",
        re.compile(r"\bdo not edit\b.*\b(generated|auto[- ]?generated)\b", re.I | re.S),
    ),
    ("generated-banner", re.compile(r"\bthis file was generated\b", re.I)),
    (
        "generated-banner",
        re.compile(
            r"\bgenerated by\s+(?:the\s+)?(?:antlr(?:\s+\d+(?:\.\d+)*)?\s+tool|"
            r"babel|bindgen|bison|cbindgen|flex|go generate|jison|peg|"
            r"protoc|protobuf|ragel|re2c|stringer)\b",
            re.I,
        ),
    ),
)


@dataclass(frozen=True)
class Reason:
    reason: str
    detail: str


@dataclass(frozen=True)
class ProtectedSet:
    active_label_files: frozenset[str]
    worthy_label_files: frozenset[str]
    frozen_duplicate_files: frozenset[str]

    @property
    def all_files(self) -> frozenset[str]:
        return frozenset(
            set(self.active_label_files)
            | set(self.worthy_label_files)
            | set(self.frozen_duplicate_files)
        )


def project_path(path: Path, project_root: Path, repos_root: Path) -> str:
    try:
        return path.resolve().relative_to(project_root.resolve()).as_posix()
    except ValueError:
        pass
    try:
        rel = path.resolve().relative_to(repos_root.resolve()).as_posix()
    except ValueError:
        return str(path)
    if rel == ".":
        return "bench/repos"
    return f"bench/repos/{rel}"


def load_pinned_repo_ids(corpus_path: Path) -> frozenset[str]:
    corpus = json.loads(corpus_path.read_text())
    repo_ids = {
        repo.get("id")
        for repo in corpus.get("repositories", [])
        if isinstance(repo.get("id"), str) and repo.get("id")
    }
    if not repo_ids:
        raise SystemExit(f"{corpus_path}: no pinned repository ids found")
    return frozenset(repo_ids)


def remove_unpinned_repo_entries(repos_root: Path, pinned_ids: frozenset[str]) -> list[Path]:
    if not repos_root.exists():
        return []
    removed: list[Path] = []
    for entry in sorted(repos_root.iterdir(), key=lambda p: p.name):
        if entry.name in pinned_ids:
            continue
        removed.append(entry)
        if entry.is_dir() and not entry.is_symlink():
            shutil.rmtree(entry)
        else:
            try:
                entry.unlink()
            except FileNotFoundError:
                pass
    return removed


def clean_unpinned_repo_entries(
    project_root: Path, repos_root: Path, corpus_path: Path
) -> list[Path]:
    pinned_ids = load_pinned_repo_ids(corpus_path)
    removed = remove_unpinned_repo_entries(repos_root, pinned_ids)
    for path in removed:
        print(f"removed unpinned corpus entry: {project_path(path, project_root, repos_root)}")
    return removed


def is_default_corpus_root(repos_root: Path, corpus_path: Path) -> bool:
    return (
        repos_root.resolve() == DEFAULT_REPOS_ROOT.resolve()
        and corpus_path.resolve() == DEFAULT_CORPUS.resolve()
    )


def normalize_label_path(path: str) -> str:
    normalized = path.replace("\\", "/")
    marker = "bench/repos/"
    idx = normalized.find(marker)
    if idx >= 0:
        return normalized[idx:]
    return normalized.removeprefix("./")


def iter_source_files(root: Path) -> Iterable[Path]:
    if not root.exists():
        return
    stack = [root]
    while stack:
        current = stack.pop()
        try:
            entries = sorted(current.iterdir(), key=lambda p: p.name)
        except OSError:
            continue
        for entry in entries:
            if entry.is_symlink():
                continue
            if entry.is_dir():
                if entry.name not in SKIP_DIRS:
                    stack.append(entry)
            elif entry.is_file() and entry.suffix.lower() in SOURCE_SUFFIXES:
                yield entry


def sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()


def file_digest(path: Path) -> tuple[str, int, str]:
    if path.is_symlink():
        target = os.readlink(path)
        digest = hashlib.sha256(target.encode("utf-8", errors="surrogateescape")).hexdigest()
        return ("symlink", len(target), digest)
    return ("file", path.stat().st_size, sha256_file(path))


def read_banner(path: Path) -> str:
    try:
        return path.read_bytes()[:BANNER_BYTES].decode("utf-8", errors="ignore")
    except OSError:
        return ""


def path_rule(path: Path, repos_root: Path) -> Reason | None:
    rel = path.resolve().relative_to(repos_root.resolve()).as_posix()
    parts = rel.split("/")
    repo_path = "/".join(parts[1:])

    if parts[0] == "black" and repo_path.startswith("src/blib2to3/"):
        return Reason("vendored-stdlib-fork", "black carries a vendored lib2to3 fork")
    if "/ragel/" in f"/{repo_path}/" or path.name.endswith((".rl.c", ".rl.cc", ".rl.cpp")):
        return Reason("ragel-output", "ragel-generated source output")
    if re.search(r"/compat/.*bsd", f"/{repo_path}", re.I):
        return Reason("bsd-compat-shim", "compatibility shim for BSD platform APIs")
    if re.search(r"/(_?unicode|emoji|entities|case[-_]?fold).*(table|data|codes)\.", f"/{repo_path}", re.I):
        return Reason("generated-data-table", "committed generated data table")
    return None


def is_generator_source(path: Path, repos_root: Path) -> bool:
    rel = path.resolve().relative_to(repos_root.resolve()).as_posix()
    _, sep, repo_path = rel.partition("/")
    if not sep:
        return False
    parts = repo_path.split("/")
    name = path.name.lower()
    if any(part in {"generator", "generators"} for part in parts):
        return True
    if name.endswith((".config.js", ".config.cjs", ".config.mjs", ".config.ts")):
        return True
    if "cmd" in parts and "generate" in parts:
        return True
    if any(part in {"scripts", "_scripts", "tools"} for part in parts) and (
        name.startswith(("gen-", "generate", "make_")) or name.startswith("_build_")
    ):
        return True
    if name.startswith("_build_") and "antlr" in name:
        return True
    return False


def prune_reason(path: Path, repos_root: Path) -> Reason | None:
    if is_generator_source(path, repos_root):
        return None
    reason = path_rule(path, repos_root)
    if reason is not None:
        return reason
    banner = read_banner(path)
    for rule_name, pattern in BANNER_RULES:
        match = pattern.search(banner)
        if match:
            detail = "matched banner text: " + " ".join(match.group(0).split())[:120]
            return Reason(rule_name, detail)
    return None


def load_protected_set(project_root: Path, labels_path: Path) -> ProtectedSet:
    active: set[str] = set()
    worthy: set[str] = set()
    if labels_path.exists():
        labels = json.loads(labels_path.read_text())
        for family in labels.get("families", []):
            members = family.get("members", [])
            for member in members:
                file_name = normalize_label_path(member.get("file", ""))
                if not file_name:
                    continue
                active.add(file_name)
                if family.get("worthy") is True:
                    worthy.add(file_name)

    frozen = load_frozen_duplicate_paths(project_root)
    return ProtectedSet(frozenset(active), frozenset(worthy), frozenset(frozen))


def load_frozen_duplicate_paths(project_root: Path) -> set[str]:
    out: set[str] = set()
    goldens = project_root / "bench" / "goldens"
    for path in sorted(goldens.glob("*.json")):
        if path.name == "corpus.json":
            continue
        try:
            payload = json.loads(path.read_text())
        except (OSError, json.JSONDecodeError):
            continue
        collect_file_paths(payload, out)
    return out


def collect_file_paths(value: object, out: set[str]) -> None:
    if isinstance(value, dict):
        for key, child in value.items():
            if key in {"file", "path"} and isinstance(child, str) and "bench/repos/" in child:
                out.add(normalize_label_path(child))
            else:
                collect_file_paths(child, out)
    elif isinstance(value, list):
        for child in value:
            collect_file_paths(child, out)


def candidate_entry(
    path: Path,
    reason: Reason,
    project_root: Path,
    repos_root: Path,
) -> dict:
    rel = project_path(path, project_root, repos_root)
    repo_rel = path.resolve().relative_to(repos_root.resolve()).as_posix()
    repo = repo_rel.split("/", 1)[0]
    stat = path.stat()
    return {
        "path": rel,
        "repo": repo,
        "repo_path": repo_rel,
        "reason": reason.reason,
        "detail": reason.detail,
        "size": stat.st_size,
        "sha256": sha256_file(path),
    }


def corpus_digest(repos_root: Path, removed: set[Path]) -> dict:
    h = hashlib.sha256()
    file_count = 0
    byte_count = 0
    removed_absolute = {p.absolute() for p in removed}
    for path in iter_all_files(repos_root):
        if path.absolute() in removed_absolute:
            continue
        rel = path.relative_to(repos_root).as_posix()
        kind, size, digest = file_digest(path)
        h.update(rel.encode("utf-8"))
        h.update(b"\0")
        h.update(kind.encode("ascii"))
        h.update(b"\0")
        h.update(str(size).encode("ascii"))
        h.update(b"\0")
        h.update(digest.encode("ascii"))
        h.update(b"\n")
        file_count += 1
        byte_count += size
    return {
        "algorithm": "sha256(repo_path NUL kind NUL size NUL file_or_symlink_sha256 LF)",
        "hex": h.hexdigest(),
        "files": file_count,
        "bytes": byte_count,
    }


def iter_all_files(root: Path) -> Iterable[Path]:
    if not root.exists():
        return
    stack = [root]
    while stack:
        current = stack.pop()
        try:
            entries = sorted(current.iterdir(), key=lambda p: p.name)
        except OSError:
            continue
        for entry in entries:
            if entry.name == ".DS_Store":
                continue
            if entry.is_symlink():
                yield entry
            elif entry.is_dir():
                if entry.name not in {".git", ".hg", ".svn"}:
                    stack.append(entry)
            elif entry.is_file():
                yield entry


def build_manifest(project_root: Path, repos_root: Path, labels_path: Path) -> tuple[dict, list[Path]]:
    protected = load_protected_set(project_root, labels_path)
    removed_entries: list[dict] = []
    skipped_entries: list[dict] = []
    removed_paths: list[Path] = []

    for path in iter_source_files(repos_root):
        reason = prune_reason(path, repos_root)
        if reason is None:
            continue
        entry = candidate_entry(path, reason, project_root, repos_root)
        if entry["path"] in protected.all_files:
            protected_by = []
            if entry["path"] in protected.active_label_files:
                protected_by.append("active-label")
            if entry["path"] in protected.worthy_label_files:
                protected_by.append("worthy-label")
            if entry["path"] in protected.frozen_duplicate_files:
                protected_by.append("frozen-duplicate")
            skipped_entries.append({**entry, "protected_by": sorted(protected_by)})
        else:
            removed_entries.append(entry)
            removed_paths.append(path)

    removed_entries.sort(key=lambda e: e["path"])
    skipped_entries.sort(key=lambda e: e["path"])
    removed_paths.sort(key=lambda p: project_path(p, project_root, repos_root))
    manifest = {
        "schema_version": "0.1.0",
        "generated_by": "bench/prune_corpus.py",
        "repos_root": project_path(repos_root, project_root, repos_root),
        "labels": project_path(labels_path, project_root, repos_root),
        "source_extensions": list(SOURCE_SUFFIXES),
        "banner_zone_bytes": BANNER_BYTES,
        "protected_sources": {
            "active_label_files": len(protected.active_label_files),
            "worthy_label_files": len(protected.worthy_label_files),
            "frozen_duplicate_files": len(protected.frozen_duplicate_files),
        },
        "totals": {
            "removed": len(removed_entries),
            "protected_skipped": len(skipped_entries),
        },
        "corpus_digest_after_prune": corpus_digest(repos_root, set(removed_paths)),
        "removed": removed_entries,
        "protected_skipped": skipped_entries,
    }
    return manifest, removed_paths


def write_manifest(path: Path, manifest: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(manifest, indent=2, sort_keys=True) + "\n")


def remove_paths(paths: Iterable[Path]) -> None:
    for path in paths:
        try:
            path.unlink()
        except FileNotFoundError:
            continue
    prune_empty_dirs({p.parent for p in paths})


def prune_empty_dirs(dirs: Iterable[Path]) -> None:
    for directory in sorted(dirs, key=lambda p: len(p.parts), reverse=True):
        current = directory
        while current.name and current.exists():
            try:
                current.rmdir()
            except OSError:
                break
            current = current.parent


def manifest_path_to_file(entry: dict, project_root: Path, repos_root: Path) -> Path:
    path = entry["path"]
    if path.startswith("bench/repos/"):
        default_repos = (project_root / "bench" / "repos").resolve()
        if repos_root.resolve() != default_repos:
            return repos_root / path.removeprefix("bench/repos/")
        return project_root / path
    return repos_root / entry["repo_path"]


def check_manifest(project_root: Path, repos_root: Path, labels_path: Path, manifest_path: Path) -> None:
    expected = json.loads(manifest_path.read_text())
    failures: list[str] = []
    for entry in expected.get("removed", []):
        path = manifest_path_to_file(entry, project_root, repos_root)
        if path.exists():
            failures.append(f"listed removal still exists: {entry['path']}")
    for entry in expected.get("protected_skipped", []):
        path = manifest_path_to_file(entry, project_root, repos_root)
        if not path.exists():
            failures.append(f"protected source is missing: {entry['path']}")

    current, _ = build_manifest(project_root, repos_root, labels_path)
    current_removed = {entry["path"] for entry in current["removed"]}
    if current_removed:
        failures.append(
            "new unpruned candidates: "
            + ", ".join(sorted(current_removed)[:10])
            + (" ..." if len(current_removed) > 10 else "")
        )
    expected_skipped = expected.get("protected_skipped", [])
    if current.get("protected_skipped", []) != expected_skipped:
        failures.append("protected_skipped entries differ from manifest")

    digest = corpus_digest(repos_root, set())
    if digest.get("hex") != expected.get("corpus_digest_after_prune", {}).get("hex"):
        failures.append(
            "corpus digest mismatch: "
            f"{digest.get('hex')} != {expected.get('corpus_digest_after_prune', {}).get('hex')}"
        )

    if failures:
        for failure in failures:
            print(f"error: {failure}", file=sys.stderr)
        raise SystemExit(1)
    print(
        "ok prune manifest: "
        f"{len(expected.get('removed', []))} removals, "
        f"digest {digest['hex'][:12]}"
    )


def repo_root_for_manifest_entry(entry: dict, repos_root: Path) -> Path | None:
    repo = entry.get("repo")
    if isinstance(repo, str) and repo:
        return repos_root / repo
    repo_path = entry.get("repo_path")
    if isinstance(repo_path, str) and repo_path:
        return repos_root / repo_path.split("/", 1)[0]
    return None


def carry_absent_removed_entries(
    project_root: Path,
    repos_root: Path,
    previous: dict,
    current: dict,
) -> dict:
    current_paths = {entry["path"] for entry in current.get("removed", [])}
    carried = []
    for entry in previous.get("removed", []):
        path = entry.get("path")
        if not isinstance(path, str) or path in current_paths:
            continue
        repo_root = repo_root_for_manifest_entry(entry, repos_root)
        if repo_root is None or not repo_root.exists():
            continue
        if manifest_path_to_file(entry, project_root, repos_root).exists():
            continue
        carried.append(entry)

    if not carried:
        return current

    merged = {
        **current,
        "removed": sorted(
            [*carried, *current.get("removed", [])],
            key=lambda entry: entry["path"],
        ),
    }
    merged["totals"] = {
        **current["totals"],
        "removed": len(merged["removed"]),
    }
    return merged


def apply_prune(
    project_root: Path, repos_root: Path, labels_path: Path, manifest_path: Path
) -> tuple[dict, int, bool]:
    previous = json.loads(manifest_path.read_text()) if manifest_path.exists() else None
    manifest, removals = build_manifest(project_root, repos_root, labels_path)
    if previous is not None:
        manifest = carry_absent_removed_entries(project_root, repos_root, previous, manifest)
        if not removals and manifest == previous:
            return previous, 0, True

    remove_paths(removals)
    write_manifest(manifest_path, manifest)
    return manifest, len(removals), False
